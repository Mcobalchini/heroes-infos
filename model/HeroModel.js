class HeroModel {
    constructor(id: Number,
                name: String,
                localizedName: String,
                accessLink: String,
                role: String,
                universe: String) {

        this.id = id
        this.name = name
        this.localizedName = localizedName
        this.accessLink = accessLink
        this.role = role
        this.universe = universe
    }
}